public class Coordinate {
	private int xValue;
	private int yValue;

	public Coordinate(int x, int y) {
		xValue = x;
		yValue = y;
	}

	public int getxValue() {
		return xValue;
	}

	public int getyValue() {
		return yValue;
	}
}
